public class printinf {

    public static void main(String[] args) {
        int      x=10;
        float   y=12.54f;
        char   z= 'A' ;

        System.out.printf("The integer values is %d \n" , x);
        System.out.printf("The integer values is %f \n" , y);
        System.out.printf("The integer values is %c \n" , z);

    }
    
}
